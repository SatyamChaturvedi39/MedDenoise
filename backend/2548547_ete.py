def evaluate_on_noise(clean, noisy, model, noise_name):
    """Evaluate denoising quality on a specific noise type."""
    denoised = model.predict(noisy, verbose=0)
    denoised = np.clip(denoised, 0.0, 1.0)
    n = len(clean)

    psnr_n = np.mean([psnr(clean[i].squeeze(), noisy[i].squeeze(), data_range=1.0) for i in range(n)])
    psnr_d = np.mean([psnr(clean[i].squeeze(), denoised[i].squeeze(), data_range=1.0) for i in range(n)])
    ssim_n = np.mean([ssim(clean[i].squeeze(), noisy[i].squeeze(), data_range=1.0) for i in range(n)])
    ssim_d = np.mean([ssim(clean[i].squeeze(), denoised[i].squeeze(), data_range=1.0) for i in range(n)])
    mse_n  = float(np.mean((clean - noisy)**2))
    mse_d  = float(np.mean((clean - denoised)**2))
    noise_reduction = (1 - mse_d / mse_n) * 100
    pixel_acc = float(np.mean(np.abs(clean - denoised) < 0.1) * 100)

    return {
        'noise_type': noise_name,
        'psnr_noisy': round(psnr_n, 2), 'psnr_denoised': round(psnr_d, 2),
        'psnr_gain': round(psnr_d - psnr_n, 2),
        'ssim_noisy': round(ssim_n, 4), 'ssim_denoised': round(ssim_d, 4),
        'ssim_gain': round(ssim_d - ssim_n, 4),
        'mse_noisy': round(mse_n, 6), 'mse_denoised': round(mse_d, 6),
        'noise_reduction_pct': round(noise_reduction, 1),
        'pixel_accuracy': round(pixel_acc, 1),
    }, denoised

