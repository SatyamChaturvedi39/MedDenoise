def augment_batch(images):
    """Apply random augmentations: flip, rotation, brightness, contrast."""
    augmented = []
    for img in images:
        h, w = img.shape[0], img.shape[1]
        # Random horizontal flip (anatomically valid for chest X-rays)
        if np.random.random() > 0.5:
            img = img[:, ::-1, :]
        # Random brightness adjustment (±15%)
        factor = np.random.uniform(0.85, 1.15)
        img = np.clip(img * factor, 0, 1)
        # Random contrast adjustment (±10%)
        mean = np.mean(img)
        contrast = np.random.uniform(0.9, 1.1)
        img = np.clip((img - mean) * contrast + mean, 0, 1)
        augmented.append(img)
    return np.array(augmented)

