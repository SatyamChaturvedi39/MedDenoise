"""
## Problem Formulation and Understanding

**Domain**: Healthcare and Wellbeing

**Problem**: Medical imaging equipment, especially portable and low-cost X-ray
machines used in rural and resource-limited hospitals, produce noisy images.
This noise can obscure critical diagnostic features — hairline fractures,
early-stage pneumonia opacities, subtle nodules — potentially leading to
misdiagnosis. Automated denoising of medical images is a pressing clinical need.

**Input**: A corrupted chest X-ray image x̃ = x + ε where ε represents
additive noise (Gaussian, impulse, or block corruption). Each image is a
128×128×1 grayscale tensor with pixel values normalized to [0, 1].

**Expected Output**: A clean reconstructed X-ray image x̂ of the same
dimensions (128×128×1), with noise artifacts removed while preserving
diagnostically relevant structures (lung fields, cardiac silhouette,
rib edges, mediastinal contours).

**Learning Task Type**: Unsupervised representation learning — a denoising
autoencoder with skip connections (U-Net) trained via a combined MSE + SSIM
reconstruction objective.

**Why U-Net over a plain Autoencoder?**
A standard autoencoder forces ALL information through a narrow bottleneck,
destroying fine anatomical detail. A U-Net adds skip connections that pass
encoder features directly to the decoder, so fine structures (rib edges,
bronchial patterns, hairline fractures) are preserved while the bottleneck
still learns to separate noise from anatomy.

**Real-World Relevance**:
- Portable X-ray machines in field hospitals produce noisy low-dose images
- Low-dose CT protocols increase quantum noise in reconstructed slices
- X-rays transmitted over low-bandwidth networks suffer compression artifacts
- WHO estimates 2/3 of the global population lacks access to quality radiology
"""

