"""
## Mathematical Modeling

**Architecture**: U-Net Denoising Autoencoder (Encoder-Decoder with Skip Connections)

**Why Skip Connections?**
In a standard autoencoder, ALL information passes through a narrow bottleneck:
  x̂ = g_φ(f_θ(x̃))

Fine details (rib edges, hairline fractures) are lost because the bottleneck
cannot preserve them. A U-Net adds skip connections that concatenate encoder
feature maps directly to decoder layers:

  d_i = g_i(concat(d_{i-1}, e_i))

where e_i is the encoder output at level i. This lets the decoder access
high-resolution features directly, so it only needs to learn the RESIDUAL
difference rather than reconstructing everything from the bottleneck.

**Loss Function**: Combined MSE + SSIM Loss
  L = α · MSE(x, x̂) + (1 - α) · (1 - SSIM(x, x̂))

where α = 0.5. MSE ensures pixel-level accuracy. SSIM preserves structural
similarity (edges, textures, luminance) — critical for medical images where
radiologists rely on structural features for diagnosis.

**Optimization**: Adam (lr=1e-3) with ReduceLROnPlateau and EarlyStopping

**Evaluation Metrics**:
- PSNR: higher = better (dB). Every +3 dB ≈ 2× quality
- SSIM: 0→1, measures structural preservation
- Pixel Accuracy: % of pixels within τ=0.1 of clean original
"""

# =====================================================================
# CELL 9 — Custom SSIM+MSE Loss Function
# =====================================================================
def ssim_loss(y_true, y_pred):
    """
    Structural Similarity loss.
    SSIM measures perceptual image quality based on luminance, contrast,
    and structure — the exact features radiologists evaluate.
    Returns 1 - SSIM so that lower loss = better structure preservation.
    """
    return 1.0 - tf.reduce_mean(
        tf.image.ssim(y_true, y_pred, max_val=1.0)
    )

def combined_loss(y_true, y_pred):
    """
    50% MSE + 50% (1 - SSIM)
    MSE handles pixel accuracy. SSIM handles structural sharpness.
    Together they produce cleaner, sharper reconstructions than either alone.
    """
    mse = tf.reduce_mean(tf.square(y_true - y_pred))
    ssim_l = ssim_loss(y_true, y_pred)
    return 0.5 * mse + 0.5 * ssim_l

# Custom metric to track SSIM during training
def ssim_metric(y_true, y_pred):
    return tf.reduce_mean(tf.image.ssim(y_true, y_pred, max_val=1.0))

# =====================================================================
# CELL 10 — Build U-Net Denoising Model
# =====================================================================
#
# U-NET ARCHITECTURE for 128×128 grayscale X-rays:
#
# ENCODER (contracting path):
#   128×128×1  → [Conv32×2 + BN] → skip1 → Pool → 64×64×32
#   64×64×32   → [Conv64×2 + BN] → skip2 → Pool → 32×32×64
#   32×32×64   → [Conv128×2 + BN + Dropout] → BOTTLENECK (32×32×128)
#
# DECODER (expanding path):
#   32×32×128  → Up → 64×64×128 → concat(skip2) → 64×64×192 → [Conv64×2]
#   64×64×64   → Up → 128×128×64 → concat(skip1) → 128×128×96 → [Conv32×2]
#   → Conv1 (sigmoid) → 128×128×1
#
# Skip connections carry encoder features directly to the decoder,
# preserving fine anatomical detail that the bottleneck would destroy.

def conv_block(x, filters, name_prefix, dropout=0.0):
    """Two Conv2D + BN + ReLU layers, optionally with Dropout."""
    x = Conv2D(filters, (3,3), padding='same', name=f'{name_prefix}_conv1')(x)
    x = BatchNormalization(name=f'{name_prefix}_bn1')(x)
    x = Activation('relu')(x)
    x = Conv2D(filters, (3,3), padding='same', name=f'{name_prefix}_conv2')(x)
    x = BatchNormalization(name=f'{name_prefix}_bn2')(x)
    x = Activation('relu')(x)
    if dropout > 0:
        x = Dropout(dropout, name=f'{name_prefix}_dropout')(x)
    return x

def build_unet_denoiser():
    inputs = Input(shape=(IMG_SIZE, IMG_SIZE, 1), name='noisy_xray_input')

    # ── ENCODER ──────────────────────────────────────────────────────
    # Level 1: 128×128 → 64×64
    enc1 = conv_block(inputs, 32, 'enc1')                    # 128×128×32
    pool1 = MaxPooling2D((2,2), name='pool1')(enc1)           # 64×64×32

    # Level 2: 64×64 → 32×32
    enc2 = conv_block(pool1, 64, 'enc2')                     # 64×64×64
    pool2 = MaxPooling2D((2,2), name='pool2')(enc2)           # 32×32×64

    # Level 3 (Bottleneck): 32×32
    bottleneck = conv_block(pool2, 128, 'bottleneck', dropout=0.15)  # 32×32×128

    # ── DECODER with Skip Connections ────────────────────────────────
    # Level 2: 32×32 → 64×64
    up2 = UpSampling2D((2,2), name='up2')(bottleneck)         # 64×64×128
    merge2 = Concatenate(name='skip2')([up2, enc2])           # 64×64×(128+64)=192
    dec2 = conv_block(merge2, 64, 'dec2')                     # 64×64×64

    # Level 1: 64×64 → 128×128
    up1 = UpSampling2D((2,2), name='up1')(dec2)               # 128×128×64
    merge1 = Concatenate(name='skip1')([up1, enc1])           # 128×128×(64+32)=96
    dec1 = conv_block(merge1, 32, 'dec1')                     # 128×128×32

    # Output: sigmoid for pixel intensities in [0, 1]
    outputs = Conv2D(1, (1,1), activation='sigmoid',
                     name='output_reconstruction')(dec1)       # 128×128×1

    model = Model(inputs, outputs, name='unet_medical_denoiser')
    model.compile(
        optimizer=tf.keras.optimizers.Adam(learning_rate=1e-3),
        loss=combined_loss,
        metrics=['mae', ssim_metric]
    )
    return model

