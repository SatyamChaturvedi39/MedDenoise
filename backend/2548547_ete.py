def add_gaussian_noise(images, noise_factor=0.3):
    """Quantum noise / electronic noise in X-ray detectors."""
    noise = noise_factor * np.random.normal(size=images.shape)
    return np.clip(images + noise, 0.0, 1.0)

def add_salt_pepper(images, amount=0.06):
    """Dead/hot pixels in flat-panel X-ray detectors."""
    noisy = images.copy()
    salt_mask = np.random.random(images.shape) < (amount / 2)
    noisy[salt_mask] = 1.0
    pepper_mask = np.random.random(images.shape) < (amount / 2)
    noisy[pepper_mask] = 0.0
    return noisy

def add_block_corruption(images, block_size=10, n_blocks=8):
    """PACS transmission errors / DICOM file corruption."""
    noisy = images.copy()
    h, w = images.shape[1], images.shape[2]
    for img in noisy:
        for _ in range(n_blocks):
            y = np.random.randint(0, h - block_size)
            x = np.random.randint(0, w - block_size)
            img[y:y+block_size, x:x+block_size] = 0.0
    return noisy

def add_mixed_noise(images):
    """Randomly apply one of three noise types to each image."""
    noisy = images.copy()
    n = len(images)
    indices = np.arange(n)
    np.random.shuffle(indices)
    third = n // 3
    # 1/3 Gaussian (varying σ)
    for i in indices[:third]:
        sigma = np.random.uniform(0.15, 0.45)
        noisy[i] = np.clip(images[i] + sigma * np.random.normal(size=images[i].shape), 0, 1)
    # 1/3 Salt & Pepper (varying amount)
    for i in indices[third:2*third]:
        amt = np.random.uniform(0.03, 0.10)
        noisy[i] = add_salt_pepper(images[i:i+1], amount=amt)[0]
    # 1/3 Block corruption (varying size)
    for i in indices[2*third:]:
        bs = np.random.randint(6, 14)
        nb = np.random.randint(5, 12)
        noisy[i] = add_block_corruption(images[i:i+1], block_size=bs, n_blocks=nb)[0]
    return noisy

